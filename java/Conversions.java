package com.conversions.java;

public class Conversions {
    public static void main(String[] args) {
        //conversion between one data type to another data type
        //automatic conversion or implicit conversion
        int a=432;
        double b;
        b=a;
        System.out.println(b);

        //Explicit conversion or manual conversion

        double c;
        c=47.7654;
        short d;
        d= (short) c;
        System.out.println(d);
    }
}
